package Bean;

public class AccountBeanNew {
	private int B_ID;
	private int CO_ID;
	private String Account_Name;
	private String Currency;
	private String Branch;
	private int Available_Balance;
	private int Opening_Balance;
	public int getB_ID() {
		return B_ID;
	}
	public void setB_ID(int b_id) {
		B_ID = b_id;
	}
	public int getCO_ID() {
		return CO_ID;
	}
	public void setCO_ID(int co_id) {
		CO_ID = co_id;
	}
	public String getAccount_Name() {
		return Account_Name;
	}
	public void setAccount_Name(String account_Name) {
		Account_Name = account_Name;
	}
	public String getCurrency() {
		return Currency;
	}
	public void setCurrency(String currency) {
		Currency = currency;
	}
	public String getBranch() {
		return Branch;
	}
	public void setBranch(String branch) {
		Branch = branch;
	}
	public int getAvailable_Balance() {
		return Available_Balance;
	}
	public void setAvailable_Balance(int available_Balance) {
		Available_Balance = available_Balance;
	}
	public int getOpening_Balance() {
		return Opening_Balance;
	}
	public void setOpening_Balance(int opening_Balance) {
		Opening_Balance = opening_Balance;
	}
}
